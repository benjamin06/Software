import java.util.ArrayList;

public class Event {
	private ArrayList<String> statut;
	private int time;
	
	
	public ArrayList<String> getStatut() {
		return statut;
	}
	public void setStatut(ArrayList<String> statut) {
		this.statut = statut;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	
}
